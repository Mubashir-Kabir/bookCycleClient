import { toast } from "react-toastify";

export const notifySuccess = (massage) => {
  toast.success(massage, {
    position: "top-center",
    autoClose: 3000,
    hideProgressBar: true,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
  });
};
